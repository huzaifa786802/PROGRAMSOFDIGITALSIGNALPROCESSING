import numpy as np
import matplotlib.pyplot as plt
x = np.array([1, 1, 2, 2])
y = np.array([1, 0, 5, 1])
r_xy = np.correlate(x, y, mode='full')
range = np.arange(-len(x) + 1, len(x))
plt.subplot(3, 1, 1)
plt.stem(x)
plt.xlabel('Sample index')
plt.ylabel('Amplitude')
plt.title('Signal x') 
plt.subplot(3, 1, 2)
plt.stem(y)
plt.xlabel('Sample index')
plt.ylabel('Amplitude')
plt.title('Signal y')
plt.subplot(3, 1, 3)
plt.stem(range, r_xy)
plt.xlabel('Lag')
plt.ylabel('Correlation')
plt.title('Cross-correlation of x and y') 
plt.tight_layout()
plt.show()